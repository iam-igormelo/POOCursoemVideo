public class Luta {
    
    // ATRIBUTOS
    private String desafiado;
    private String desafiante;
    private int rounds;
    private String aprovada;


    // METODOS
    public void marcarLuta() {}

    public void lutar() {}


    // GETTERS AND SETTERS
    public String getDesafiado() {
        return desafiado;
    }
    public void setDesafiado(String desafiado) {
        this.desafiado = desafiado;
    }
    public String getDesafiante() {
        return desafiante;
    }
    public void setDesafiante(String desafiante) {
        this.desafiante = desafiante;
    }
    public int getRounds() {
        return rounds;
    }
    public void setRounds(int rounds) {
        this.rounds = rounds;
    }
    public String getAprovada() {
        return aprovada;
    }
    public void setAprovada(String aprovada) {
        this.aprovada = aprovada;
    }

}
